def calculate_hand_value(hand):
    value = 0
    num_aces = 0

    for card in hand:
        rank = card[0]
        if rank == 'A':
            value += 11
            num_aces += 1
        elif rank in ['K', 'Q', 'J']:
            value += 10
        else:
            value += int(rank)
    while value > 21 and num_aces > 0:
        value -= 10
        num_aces -= 1

    return value


def format_hand(hand):
    formatted_hand = []
    for card in hand:
        rank = card[0]
        suit = card[1]
        if suit == "diamonds":
            suit = ":diamonds:"
        elif suit == "hearts":
            suit = ":hearts:"
        elif suit == "clubs":
            suit = "<:clubs:1111111038222483536>"
        else:
            suit = "<:spades:1111110989673406555>"

        if rank == 'A':
            formatted_hand.append("Ace " + suit)
        elif rank == 'K':
            formatted_hand.append("King " + suit)
        elif rank == 'Q':
            formatted_hand.append("Queen " + suit)
        elif rank == 'J':
            formatted_hand.append("Jack" + suit)
        else:
            formatted_hand.append(f"{rank} {suit}")
    return formatted_hand


